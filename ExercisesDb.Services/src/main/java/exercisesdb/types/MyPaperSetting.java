package exercisesdb.types;

public class MyPaperSetting {
    private Integer title;
    private Integer paperInfo;
    private Integer inputInfo;
    private Integer tongfen;
    private Integer pingfen;
    private Integer answer;
    private Integer defen;
    private Integer fileKind;
    private Integer pageSize;

    public Integer getTitle() {
        return title;
    }

    public void setTitle(Integer title) {
        this.title = title;
    }

    public Integer getPaperInfo() {
        return paperInfo;
    }

    public void setPaperInfo(Integer paperInfo) {
        this.paperInfo = paperInfo;
    }

    public Integer getInputInfo() {
        return inputInfo;
    }

    public void setInputInfo(Integer inputInfo) {
        this.inputInfo = inputInfo;
    }

    public Integer getTongfen() {
        return tongfen;
    }

    public void setTongfen(Integer tongfen) {
        this.tongfen = tongfen;
    }

    public Integer getPingfen() {
        return pingfen;
    }

    public void setPingfen(Integer pingfen) {
        this.pingfen = pingfen;
    }

    public Integer getAnswer() {
        return answer;
    }

    public void setAnswer(Integer answer) {
        this.answer = answer;
    }

    public Integer getDefen() {
        return defen;
    }

    public void setDefen(Integer defen) {
        this.defen = defen;
    }

    public Integer getFileKind() {
        return fileKind;
    }

    public void setFileKind(Integer fileKind) {
        this.fileKind = fileKind;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }
}
